#!/usr/bin/env python3
import os
import sys
import json
import argparse
import datetime
import platform
import socket

def main():
    parser = argparse.ArgumentParser(description="Document Nextflow run parameters and environment metadata.")
    parser.add_argument("--outdir", required=True, help="Output results directory")
    parser.add_argument("--fastq-dir", required=True, help="Input FASTQ directory")
    parser.add_argument("--cohort", default="Minerva Cohort", help="Cohort name/description")
    args = parser.parse_args()

    outdir = os.path.abspath(args.outdir)
    fastq_dir = os.path.abspath(args.fastq_dir)

    if not os.path.exists(outdir):
        os.makedirs(outdir, exist_ok=True)

    # Detect sample subdirectories
    samples = []
    if os.path.exists(fastq_dir):
        for item in sorted(os.listdir(fastq_dir)):
            item_path = os.path.join(fastq_dir, item)
            if os.path.isdir(item_path):
                # Count fastq files
                fastqs = [f for f in os.listdir(item_path) if f.endswith(".fastq.gz") or f.endswith(".fq.gz")]
                if fastqs:
                    samples.append({
                        "sample_id": item,
                        "fastq_count": len(fastqs)
                    })

    now_iso = datetime.datetime.now(datetime.timezone.utc).isoformat()
    now_str = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    params_dict = {
        "timestamp_utc": now_iso,
        "date_formatted": now_str,
        "cohort": args.cohort,
        "hostname": socket.gethostname(),
        "platform": platform.platform(),
        "paths": {
            "input_fastq_dir": fastq_dir,
            "output_results_dir": outdir,
            "rrna_ref": "/datos/migccl/rRNA_refs/human_rRNA.fasta",
            "fusioncatcher_dir": "/datos/migccl/fusioncatcher_data",
            "arriba_refs": "/datos/migccl/arriba_refs/"
        },
        "pipeline_parameters": {
            "methods_executed": ["arriba_starfusion", "fusioncatcher"],
            "filter_rrna": True,
            "run_allsorts": True,
            "allsorts_strand": "reverse",
            "skip_fastqc": True,
            "threads_per_task": 16,
            "ram_allocated": "90.GB",
            "star_multimap_nmax": 50,
            "star_chim_out_type": "WithinBAM HardClip"
        },
        "samples_processed": samples,
        "total_samples": len(samples)
    }

    # Write JSON metadata
    json_path = os.path.join(outdir, "run_parameters.json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(params_dict, f, indent=2)

    # Write Markdown documentation
    md_path = os.path.join(outdir, "RUN_PARAMETERS.md")
    sample_rows = "\n".join([f"| `{s['sample_id']}` | {s['fastq_count']} FASTQ files |" for s in samples])

    md_content = f"""# Run Parameter Audit Log — {args.cohort}

**Execution Date:** {now_str} (UTC: `{now_iso}`)  
**Host System:** `{socket.gethostname()}` ({platform.platform()})  
**Cohort Name:** {args.cohort}

---

## 1. Directory Configurations
* **Input FASTQ Directory:** `{fastq_dir}`
* **Output Results Directory:** `{outdir}`
* **rRNA Reference FASTA:** `/datos/migccl/rRNA_refs/human_rRNA.fasta`
* **FusionCatcher Database:** `/datos/migccl/fusioncatcher_data`
* **Arriba / STAR Reference Bundle:** `/datos/migccl/arriba_refs/`

---

## 2. Pipeline Parameters & Configuration

| Parameter | Configured Value | Description |
| :--- | :--- | :--- |
| **Methods Executed** | `arriba_starfusion`, `fusioncatcher` | Callers run (shared STAR alignment for Arriba & STAR-Fusion) |
| **rRNA Filtering (`--filter_rrna`)** | `true` | Pre-filtering reads against rRNA reference with BBDuk |
| **rRNA Reference (`--rrna_ref`)** | `/datos/migccl/rRNA_refs/human_rRNA.fasta` | Human rRNA reference FASTA |
| **ALLSorts Subtype Classification** | `true` | B-ALL subtype prediction from STAR gene counts |
| **ALLSorts Strand** | `reverse` | ReadsPerGene strand column |
| **FastQC / MultiQC** | `skip_fastqc: true` | FastQC / MultiQC steps skipped during fusion call pass |
| **CPUs per process** | `16` | Allocated CPU threads |
| **RAM per process** | `90.GB` | Memory allocation |
| **STAR Chimera Output Type** | `WithinBAM HardClip` | Shared STAR alignment output mode |
| **STAR Multimap Limit** | `50` | Multimap nmax setting for complex rearrangements |

---

## 3. Samples Included in this Run ({len(samples)} total)

| Sample ID | FASTQ Files Found |
| :--- | :--- |
{sample_rows}

---

*This document was automatically generated by `nextflow_pipeline/bin/document_run_params.py`.*
"""

    with open(md_path, "w", encoding="utf-8") as f:
        f.write(md_content)

    print(f"Run parameters documented successfully in:\n  - {md_path}\n  - {json_path}")

if __name__ == "__main__":
    main()
